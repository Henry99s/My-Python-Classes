def aumentar(valor, porcentagem):
    aumento = valor + (valor * porcentagem / 100)
    return aumento


def diminuir(valor, porcentagem):
    diminui = valor - (valor * porcentagem / 100)
    return diminui


def dobro(valor):
    resultado = valor * 2
    return resultado


def metade(valor):
    resultado = valor / 2
    return resultado
